    public String setValorFuenteSistema() {
        if (this.jrbt_BasesDeDatos.isSelected())//Escuchaseleccion
        {
            valorFuente = "" + 3;

        } else if (this.jrbt_Xml.isSelected()) //Escuchaseleccion
        {
            valorFuente = "" + 2;

        }
        if (this.jrbt_ArchivosPlanos.isSelected())//Escuchaseleccion 
        {
            valorFuente = "" + 1;

        }

        return valorFuente;

    }

 

